// Coro;
function calcular_coro () {
	let num = document.getElementById("numero_coro").value;
	const coro = 220
	if (num <= 0) {
		document.getElementById("resultado_coro").innerHTML = "Por favor, insira um valor positivo, diferente de zero";
		resultado_coro.setAttribute("style", "color: red")
		document.getElementById("numero_coro").focus()
		} else {
		let resultado = num * coro;
		document.getElementById("resultado_coro").innerHTML = "O resultado do cálculo é " + resultado + " l";
		resultado_coro.style.color = "green";
	}
}

